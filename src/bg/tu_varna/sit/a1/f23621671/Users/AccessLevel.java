/**
 * Enum representing different levels of user access.
 */
public enum AccessLevel {
    /**
     * Regular user with standard access permissions.
     */
    USER,

    /**
     * Administrator with elevated privileges.
     */
    ADMINISTRATOR,

    /**
     * No access granted.
     */
    NONE
}